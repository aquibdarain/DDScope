        // Function to update the "To" datetime based on selected duration and "From" datetime
        function updateToDateTime() {
            var fromDateTime = document.getElementById("fromDateTime").value;
            var duration = parseInt(document.querySelector('input[name="duration"]:checked').value);

            var fromDate = new Date(fromDateTime);
            var toDateTime = new Date(fromDate.getTime() + duration * 60000); // Adding duration in milliseconds

            var toYear = toDateTime.getFullYear();
            var toMonth = ('0' + (toDateTime.getMonth() + 1)).slice(-2);
            var toDate = ('0' + toDateTime.getDate()).slice(-2);
            var toHours = ('0' + toDateTime.getHours()).slice(-2);
            var toMinutes = ('0' + toDateTime.getMinutes()).slice(-2);
            //var toSeconds = ('0' + toDateTime.getSeconds()).slice(-2); // Get seconds

            //var toDateTimeString = toYear + '-' + toMonth + '-' + toDate + 'T' + toHours + ':' + toMinutes + ':' + '00'; // Include seconds

            var toDateTimeString = toYear + '-' + toMonth + '-' + toDate + 'T' + ('0' + toHours).slice(-2) + ':' + toMinutes;

            document.getElementById("toDateTime").value = toDateTimeString;
        }
    </script>


